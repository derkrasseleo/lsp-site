<p>The developers spent a couple of time to make plugins, so it could be nice to convert the spent time into some music stuff or studio gear.</p>
<p>Unless many commercial or proprietary projects, LSP project does not sell license keys or offer technical support for enterprise solutions.</p>
<p>The final donation goal for each plugin is comparable to (and in most cases less than) the monthly salary of the Software Engineer outside the United States and Silicon Valley.</p>
<p>For example, $1000 donated to the project is less than 10 individual license purchases of the <a href="http://overtonedsp.co.uk/download/download_af210/index.html">OverTone DSP AF2-10/M Stereo Graphical EQ</a> plugin and
is the price for only one <a href="http://global.focusrite.com/firewire-audio-interfaces/liquid-saffire-56">Focusrite Liquid Saffire 56</a> audio interface.</p>