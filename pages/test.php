<h1>DOWNLOAD</h1>

<p>The LSP project is an open-source project and cares about quality of developed software.</p>
<p>Still there is no absolute warranty about stability of the software on different platforms, so you're using this software on your own risk.</p>
<p>The source code takes a lot of time to develop (instead of drinking beer or doing something else) so it's fair when the spent time is paid.</p>
<p>That's why the project uses donation model: the binary distributions of the software are completely free but the source code for each plugin will be published under the free license only after the donation goal is reached.</p>
<p>On this page you may download the latest binary releases, get the published sources and donate to the unpublished sources to make them published.</p>

